#define OLED_SCLK_CLR()         (OLED_SCL = 0)
#define OLED_SCLK_SET()         (OLED_SCL = 1)
#define OLED_SDIO_CLR()         (OLED_SDA = 0)
#define OLED_SDIO_SET()         (OLED_SDA = 1)


void delay_ms(uint16_t ms)
{
        uint16_t i;
        while (ms) {
                i = 1800;
                while (i--);
                ms--;
        }
}


/*******************************************************************************
        => IIC_START
*******************************************************************************/
void iic_start(void)
{
        OLED_SCLK_SET();
        OLED_SDIO_SET();
        OLED_SDIO_CLR();
        //对于8051单片这里不需延时
        OLED_SCLK_CLR();
}

/*******************************************************************************
        => IIC_STOP
*******************************************************************************/
void iic_stop(void)
{
        OLED_SCLK_SET();
        OLED_SDIO_CLR();
        //对于8051单片这里不需延时
        OLED_SDIO_SET();
}


