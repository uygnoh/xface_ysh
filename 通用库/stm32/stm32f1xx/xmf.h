#ifndef __XMF_H__
#define __XMF_H__

// 比较两个数， 返回最小的那个数
//_____________________________________________________________
#ifndef XMF_MINIMUM
#define XMF_MINIMUM(a, b)       (((a) < (b)) ? (a) : (b))
#endif

// 比较两个数， 返回最大的那个数
//_____________________________________________________________
#ifndef XMF_MAXIMUM
#define XMF_MAXIMUM(a, b)       (((a) > (b)) ? (a) : (b))
#endif

#endif
